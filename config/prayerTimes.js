// config/prayerTimes.js
// Примерные времена намаза для Душанбе (UTC+5)
// В реальном проекте можно обновлять раз в месяц или использовать API

const PRAYER_TIMES = {
  // Ключ: месяц (0-11), значение: [Фаджр, Зухр, Аср, Магриб, Иша] — часы и минуты
  0:  [6,15, 12,30, 15,30, 17,45, 19,15], // январь
  1:  [6,00, 12,30, 16,00, 18,15, 19,45],
  2:  [5,30, 12,20, 16,30, 18,45, 20,15],
  3:  [4,45, 12,15, 17,00, 19,15, 20,45],
  4:  [4,15, 12,10, 17,30, 19,45, 21,15],
  5:  [3,50, 12,10, 18,00, 20,15, 21,45],
  6:  [3,45, 12,20, 18,30, 20,30, 22,00],
  7:  [4,00, 12,25, 18,30, 20,15, 21,45],
  8:  [4,30, 12,20, 17,45, 19,30, 21,00],
  9:  [5,00, 12,10, 17,00, 18,45, 20,15],
  10: [5,30, 12,00, 16,15, 17,45, 19,15],
  11: [5,50, 12,00, 15,30, 17,15, 18,45],
};

function getPrayerTimes(date = new Date()) {
  const month = date.getMonth();
  const base = PRAYER_TIMES[month];
  if (!base) return null;
  // Возвращаем массив объектов { name, hour, minute }
  const names = ['Фажр', 'Зухр', 'Аср', 'Магриб', 'Иша'];
  return names.map((name, i) => ({
    name,
    hour: base[i * 2],
    minute: base[i * 2 + 1]
  }));
}

module.exports = { getPrayerTimes };
